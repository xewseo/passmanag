package com.example.passmanag;

public class ApplicationData {
    private String application;
    private String username;
    private String password;
    private String typecipher;
    private String unikal;
    private String kluch;

    public ApplicationData() {
        this.application = application;
        this.username = username;
        this.password = password;
        this.typecipher = typecipher;
        this.unikal = unikal;
        this.kluch = kluch;
    }
    public String getApplication() {
        return application;
    }
    public void setApplication(String application) {
        this.application = application;
    }
    public String getUsername() {
        return username;
    }
    public void setUsername(String username) {
        this.username = username;
    }
    public String getPassword() {
        return password;
    }
    public void setPassword(String password) {
        this.password = password;
    }
    public String getTypecipher() {
        return typecipher;
    }
    public void setTypecipher(String typecipher) {
        this.typecipher = typecipher;
    }
    public String getUnikal() {
        return unikal;
    }
    public void setUnikal(String unikal) {
        this.unikal = unikal;
    }
    public String getKluch() {
        return kluch;
    }
    public void setKluch(String kluch) {
        this.kluch = kluch;
    }
}
